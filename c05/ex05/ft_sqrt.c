#include <stdio.h>

int	ft_sqrt(int nb)
{
	long int	i;

	if (nb < 0)
		return (0);
	if (nb == 1)
		return (1);
	i = 2;
	if (i >= 2)
	{
		while (i * i <= nb)
		{
			if ((i * i) == nb)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
